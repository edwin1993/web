              <section class="servicio" id="s3">
               <div class="title-service">
                 ESTRUCTURACIÓN DE PROYECTOS
               </div>
               <figure><img src="img/Servicios/3.jpg"  alt="Estructuración de Proyectos"></figure>
               <article>
                 <h3>SOBRE EL SERVICIO</h3>

                 <p>La estructuración de proyectos consiste en el desarrollo de todas las actividades tendientes a lograr un consenso de voluntades entre los intervinientes de un proyecto, bajo un esquema económico, financiero, técnico, jurídico y comercial exitoso. Es en el proceso de estructuración donde normalmente se vincula el propietario del terreno, el constructor, el gerente, el comercializador, los diseñadores, los inversionistas, el banco, entre otros.</p>

                 <h3>SOBRE NUESTRA EXPERIENCIA</h3>

                 <p>Se cuenta con la experiencia de haber estructurado cerca de 90 proyectos a lo largo del país, proyectos en los que la compañía ha participado en por lo menos alguno de los roles principales e incluso únicamente como estructurador. El proyecto estructurado más reciente culminó con la subasta de un extraordinario predio industrial y comercial en las afueras de Barranquilla, donde una reconocida firma constructora hizo la mejor oferta y desarrollará un proyecto de alta valorización y dinamismo comercial.</p>

                 <h3>SOBRE LAS TARIFAS</h3>

                 <p>Los honorarios por este servicio fluctúan entre el 1.5 y el 2% de las ventas del proyecto, dependiendo de las características del mismo y del alcance que se defina para la labor.</p>


               </article>
             </section>
              <section class="servicio" id="s4">
               <div class="title-service">
                 COMERCIALIZACIÓN DE PROYECTOS
               </div>
               <figure><img src="img/Servicios/4.jpg"  alt="Comercialización de Proyectos"></figure>
               <article>
                 <h3>SOBRE EL SERVICIO</h3>

                 <p>La comercialización de proyectos consiste en la ejecución de todas las labores comerciales del proyecto tendientes a administrar los potenciales compradores y las obligaciones que estos adquieran al vincularse al proyecto. El alcance del trabajo va desde la asesoría comercial en la etapa de diseño arquitectónico hasta la entrega de los inmuebles a los compradores.</p>

                 <h3>SOBRE NUESTRA EXPERIENCIA</h3>

                 <p>La mayoría de los proyectos comercializados, propios y de terceros, se han desarrollado a través de la firma aliada Estrategias Comerciales y de Mercadeo SAS, está firma ha participado en más de 150 proyectos inmobiliarios de diferentes características, principalmente de vivienda de estrato alto en Bogotá. Adicionalmente esta inmobiliaria ha creado el Show Room de Cartagena y el Caribe, donde se han comercializado más de 25 de los proyectos más exclusivos de ese sector del país.</p>

                 <h3>SOBRE LAS TARIFAS</h3>

                 <p>La tarifa oscila entre el 3 y el 5% de las ventas del proyecto y depende de las características del mismo.</p>


