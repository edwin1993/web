              <section class="servicio" id="s5">
               <div class="title-service">
                 DISEÑO ARQUITECTÓNICO
               </div>
               <figure><img src="img/Servicios/5.jpg"  alt="Diseño Arquitectónico"></figure>
               <article>
                 <h3>SOBRE EL SERVICIO</h3>

                 <p>Este servicio consiste en la elaboración de todos los planos arquitectónicos necesarios para la construcción del proyecto, siguiendo los parámetros establecidos por la gerencia o el dueño. El servicio incluye un esquema básico, un anteproyecto arquitectónico y el proyecto arquitectónico definitivo, y el alcance va desde los planos de localización, cortes, fachadas y elevaciones, hasta los detalles arquitectónicos. A su turno, incluye la coordinación técnica de planos con los demás diseñadores del proyecto y un acompañamiento en la gestión de aprobación de los diseños ante las entidades competentes.</p>

                 <h3>SOBRE NUESTRA EXPERIENCIA</h3>

                 <p>Más de 80 proyectos diseñados (1´200.000 m2 aproximadamente) a Diciembre de 2013. Principalmente proyectos de vivienda de estrato medio y medio alto en Bogotá, pero también se han realizado diseños de áreas comerciales, centros empresariales, restaurantes, oficinas, viviendas VIS, edificios sostenibles, casas campestres, hoteles y arquitectura interior. El proyecto arquitectónico más reciente es el ejecutado con Moratto 44, proyecto de vivienda estrato 6 en Bucaramanga, inspirado en un concepto innovador en materia de sostenibilidad.</p>

                 <h3>SOBRE LAS TARIFAS</h3>

                 <p>Las tarifas serán de acuerdo con las establecidas por la Sociedad Colombiana de Arquitectos, oscilan entre el 3 y el 3.5% del costo directo de la obra.</p>



               </article>
