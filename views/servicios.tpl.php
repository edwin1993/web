                 <h3>SOBRE NUESTRA EXPERIENCIA</h3>

                 <p>Cerca de 80 proyectos gerenciados (1´300.000 m2 aproximadamente) a Diciembre de 2013. Principalmente proyectos de vivienda de estrato medio y medio alto en Bogotá, pero también se han gerenciado proyectos comerciales, oficinas, viviendas VIS, casas campestres y hoteles. La ejecución más reciente de este servicio es la del proyecto Baia 138, un proyecto de 88 apartamentos en Cedritos, Bogotá, con espectaculares zonas comunes incluida una pista atlética de 100 metros en una cubierta sostenible.</p>

                 <h3>SOBRE LAS TARIFAS</h3>

