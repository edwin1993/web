              <section class="servicio" id="s2">
               <div class="title-service">
                 CONSTRUCCIÓN DE EDIFICACIONES
               </div>
               <figure><img src="img/Servicios/2.jpg"  alt="Construcción de Edificaciones"></figure>
               <article>
                 <h3>SOBRE EL SERVICIO</h3>

                 <p>Consiste en llevar a cabo los trabajos correspondientes a la ejecución de la obra, teniendo en cuenta principalmente los diseños, la programación y el presupuesto del proyecto. El servicio se desarrolla mediante los sistemas de administración delegada, a precio global fijo o por precios unitarios, entre otros, y tiene como alcance la dirección de la construcción, la administración técnica de subcontratistas, proveedores y labores relacionadas con las compras y contrataciones.</p>

                 <h3>SOBRE NUESTRA EXPERIENCIA</h3>

                 <p>Más de un centenar de proyectos construidos (1´500.000 m2 aproximadamente) a Diciembre de 2013. Principalmente proyectos de vivienda de estrato medio y medio alto en Bogotá, pero también se ha ejecutado la construcción de áreas comerciales, centros empresariales, restaurantes, oficinas, viviendas VIS, casas campestres, hoteles y remodelaciones. El proyecto de construcción más reciente es Irotama Reservado, proyecto exclusivo de apartamentos de alta gama en el sector de Pozos Colorados, Santa Marta, dentro del complejo de Irotama Resort.</p>

                 <h3>SOBRE LAS TARIFAS</h3>

                 <p>Bajo el sistema de administración delegada, la tarifa corresponde al 10% del costo directo del proyecto, en los demás sistemas, los costos de las actividades obedecerán a los del mercado.</p>

               </article>
