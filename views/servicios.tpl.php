                 INTERVENTORÍAS DE OBRA
               </div>
               <figure><img src="img/Servicios/1.jpg"  alt="Inventoría de Obra"></figure>
               <article>
                 <h3>SOBRE EL SERVICIO</h3>

                 <p>El servicio de interventoría de obra consiste en realizar las labores de control y supervisión de una obra, acorde a los objetivos planteados por el propietario. La interventoría integral incluye todos los aspectos administrativos, técnicos y económicos de la obra, aunque su alcance puede ser modificado.</p>
                 <h3>SOBRE NUESTRA EXPERIENCIA</h3>

                 <p>Se han realizado cerca de 15 interventorías de obra (225.000 m2 aproximadamente) a Diciembre de 2013, cada una de ellas con diferentes alcances. La última interventoría de obra fue la realizada en el proyecto Sierras del Este en Bogotá, proyecto exitoso de apartamentos de estrato 4, de alta valorización, ubicado a la altura de la calle 63 con avenida circunvalar.</p>

                 <h3>SOBRE LAS TARIFAS</h3>

                 <p>La tarifa correspondiente a este servicio se rige por lo dispuesto en el Decreto 2090 de 1989 y es del orden del 2 % de los costos directos de la obra.</p>

               </article>
